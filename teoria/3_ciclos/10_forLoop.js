"use strict";
// for (inicializar; condicion; incremento)
for (let iFor = 0; iFor <= 10; iFor++) {
    console.log(iFor);
}
// el for se tiene como contador, es decir, el inicializador es un numero, en el while puede ser otra tipo de datos
// se usa for cuando conoces el numero total de elementos a iterar, es decir, elementos de longitud fija
